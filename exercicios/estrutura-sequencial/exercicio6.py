#Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.

# Recebendo o valor do raio e atribuindo a variável
raio = float(input('Informe o valor do raio de um círculo (cm): '))

# Calculando e atribuindo a área do círculo
area = raio * 3.14**2

# Imprimindo a área do círculo
print("Área do círculo: %.2f cm" %area)

"""
Faça um algoritmo que solicite ao usuário informar 3 números.
Em seguida, some-os e envie para a saída padrão a seguinte
frase: "A soma total é: "
"""

num1 = float(input("Informe um número: "))
num2 = float(input("Informe outro número: "))
num3 = float(input("Informe mais um número: "))

print("A soma total é:", (num1 + num2 + num3))
